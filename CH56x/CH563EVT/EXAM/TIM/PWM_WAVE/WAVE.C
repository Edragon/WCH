/*******************************************************************************
* Function Name  : filehead_load
* Description    : 计算PWM占空比的值
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void filehead_load( UINT8 *pfile)
{
     UINT8    *p;

    
    filesize = fetch4(pfile+0x04);                                              /* 取文件大小 */

    /*取fmt段的大小,及通道数，取样位数，取样频率 */
    fmtsize = fetch4(pfile+0x10);                                               /* 18 过滤字节 */
    Channel=fetch2(pfile+0x16);                                                 /* 1  声道数量 */
    Samplerate=fetch2(pfile+0x18);                                              /* 11025 采样率 */
    Samplebit=fetch2(pfile+0x22);                                               /* 8 采样位数 */
    /* 取data段的大小，之前先判断是否有fact段，fact段为12个字节 */
    p = pfile;
    p = p+0x14+fmtsize;
    if(*p == 0x66){                                                             /* 判断段的首字母，如果是f，则为fact段，如果是d，就是data段 */
        p=p+16;
        pdata_addr=p+4;
        datasize = fetch4(p);
    }
    else{
        p=p+4;
        pdata_addr=p+4;
        datasize = fetch4(p);
     }
     CountEnd = 100000000/(4*Samplerate);                                       /* 根据前面提取的采样率计算出在100MHz主频下的CH563通过PWM产生方波的计数终止值 */
}

/*******************************************************************************
* Function Name  : fetch2
* Description    : 以paddr作为一个16位数据的首地址，采用小端的格式，提取出这个16位的数
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
UINT16 fetch2(UINT8  *paddr)
{
    UINT16 comdata;

    paddr=paddr+1;
    comdata=*paddr;
    paddr--;
    comdata=comdata<<8;
    comdata=comdata+ *paddr;
    return comdata;
}

/*******************************************************************************
* Function Name  : fetch4
* Description    : 以paddr作为一个32位数据的首地址，采用小端的格式，提取出这个32位的数
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
UINT32 fetch4(UINT8  *paddr)
{
    UINT32 comdata;

    paddr=paddr+3;
    comdata=*paddr;
    paddr--;
    comdata=comdata<<8;
    comdata=comdata+ *paddr;
    paddr--;
    comdata=comdata<<8;
    comdata=comdata+ *paddr;
    paddr--;
    comdata=comdata<<8;
    comdata=comdata+ *paddr;

    return comdata;
}

